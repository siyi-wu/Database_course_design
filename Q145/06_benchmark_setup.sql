USE <database_name>;

DROP TABLE IF EXISTS benchmark_borrowrecords;
DROP TABLE IF EXISTS benchmark_equipments;

CREATE TABLE benchmark_equipments (
    equip_id INT NOT NULL,
    equip_name VARCHAR(100) NOT NULL,
    category_id INT NOT NULL,
    room_id INT NOT NULL,
    status ENUM('Available', 'Borrowed', 'Maintenance', 'Scrapped') DEFAULT 'Available',
    price DECIMAL(10, 2),
    purchase_date DATE,
    PRIMARY KEY (equip_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE benchmark_borrowrecords (
    record_id INT NOT NULL,
    equip_id INT NOT NULL,
    user_id INT NOT NULL,
    borrow_date DATE NOT NULL,
    plan_return_date DATE NOT NULL,
    actual_return_date DATE DEFAULT NULL,
    PRIMARY KEY (record_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

